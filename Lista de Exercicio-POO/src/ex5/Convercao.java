package ex5;

public class Convercao {

	double metros, milhas, pes;

	public Convercao(double metros, double milhas, double pes) {
		this.metros = metros;
		this.milhas = milhas;
		this.pes = pes;
	}

	public double getMetros() {
		return metros;
	}

	public void setMetros(double metros) {
		this.metros = metros ;
	}

	public double getMilhas() {
		return milhas;
	}

	public void setMilhas(double milhas) {
		this.milhas = milhas;
	}

	public double getPes() {
		return pes;
	}

	public void setPes(double pes) {
		this.pes = pes;
	}
	
	
	
}
